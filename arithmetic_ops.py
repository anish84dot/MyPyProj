var1 = 25
var2 = 15

#addition, using another variable to store the sum
sum =  var1 + var2
print("Sum of", var1, "and", var2, ":", sum)
#subtraction, without using intermedite variable
print("Difference of", var1, "and", var2, ":", var1 -  var2)

#product
print(var1, "*", var2, "=", var1 * var2)

#power - exponentiation
print(var1, "**", var2, "=", var1 ** var2)

#float division
print(var1, "/", var2, "=", var1 / var2)

#integer division
print(var1, "//", var2, "=", var1 // var2)

#modulus - remainder
print(var1, "%", var2, "=", var1 % var2)
